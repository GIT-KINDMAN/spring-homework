package com.douzone.homework.controller;

public class StudentForm {
    private String name;
    private int music;
    private int art;
    private int ath;
    private int sum;
    private double avg;

    public int getMusic() {
        return music;
    }

    public void setMusic(int music) {
        this.music = music;
    }

    public int getArt() {
        return art;
    }

    public void setArt(int art) {
        this.art = art;
    }

    public int getAth() {
        return ath;
    }

    public void setAth(int ath) {
        this.ath = ath;
    }

    public int getSum() {
        return sum;
    }

    public void setSum(int sum) {
        this.sum = sum;
    }

    public double getAvg() {
        return avg;
    }

    public void setAvg(double avg) {
        this.avg = avg;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
